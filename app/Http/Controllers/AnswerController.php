     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = Auth::user()->id;
        $existingSurvey = Answer::where('survey_id', $request['response'][0]['survey_id'])->where('user_id', $user)->first();
        if ($existingSurvey) {
            return redirect()->back()->with('error', 'You have already answer on this survey.'); 
        }
        Answer::insert($request['response']);
        return redirect()->back()->with('success', 'Your Survey Send Successfully');
    }

